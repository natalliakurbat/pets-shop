const cardTemplate = document.querySelector('#item-template'); //обращаемся к шаблону теплате
const shopItems = document.querySelector('#shop-items'); //обращаемся к shop-items потому что туда нам потом нужно закинуть отсартированные объекты

items.forEach(i => { //используем forEach для перебора объекта (стрелочная функция)
    const myCard = cardTemplate.content.cloneNode(true); //с помощью content обращаемся к шаблону, true копируем вложенные в шаблон теги
    // помещаем ключи в соответ теги :
    myCard.querySelector('h1').textContent = i.title;
    myCard.querySelector('p').textContent = i.description;
    myCard.querySelector('img').src = i.img; //т.к. ссылка для изображения то пишем src
    myCard.querySelector('.price').textContent = `${i.price}p`; //к значению нужно приписать р
    myCard.querySelector('.tags').textContent = i.tags;

    shopItems.append(myCard); //вставляем в конец контейнера 
})